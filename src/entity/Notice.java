package entity;

public class Notice {
String title;
String writerid;
String regdate;
String git;
String files;
String content;
public Notice(String title, String writerid, String regdate, String git, String files, String content) {
	super();
	this.title = title;
	this.writerid = writerid;
	this.regdate = regdate;
	this.git = git;
	this.files = files;
	this.content = content;
}


}
